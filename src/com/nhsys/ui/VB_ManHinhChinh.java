    public void reloadPanelMenu(int i) {
        jpnCenterMHC.removeAll();
        switch (i) {
            case 1:
                QL_MenuNV = new QL_MenuNV();
                jpnCenterMHC.add(QL_MenuNV);

                break;
            case 2:
                QL_MenuQL = new QL_MenuQL();
                jpnCenterMHC.add(QL_MenuQL);
                break;
        }
        jpnCenterMHC.updateUI();
    }

